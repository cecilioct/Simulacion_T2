    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        JFileChooser filechooser = new JFileChooser();
        filechooser.setFileFilter(filter);
        int opcion = filechooser.showOpenDialog(this);
        
        if(opcion==JFileChooser.APPROVE_OPTION){
        RUTA = filechooser.getSelectedFile().toString();
           
        
        }
        
        csVarianza obj = new csVarianza(0,RUTA);
        obj.LeerExcel(RUTA);
        mostrar(obj.vector(),obj.getNumfil(),obj.getNumCols());
        
        
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int nivel = Integer.parseInt(txtporcenta.getText());
        csVarianza obj = new csVarianza(nivel,RUTA);
        
        obj.LeerExcel(RUTA);
        obj.calcularMedia();
        obj.LeerExcel(RUTA);
        obj.calcularVarianza();
        obj.alpha();
        obj.equivalente();
        obj.calcLI();
        obj.calcLS();
        
        txtLS.setText(Double.toString(obj.getLS()));
        txtLI.setText(Double.toString(obj.getLI()));
        txt_varianza.setText(Double.toString(obj.getVarianza()));
        
        boolean op = obj.verificar();
        if(op==true){
         txt_fin.setText(Double.toString(obj.getLI())+" > "+Double.toString(obj.getVarianza())+" < "+Double.toString(obj.getLS()));

        }else if(op==false){
          txt_fin.setText(Double.toString(obj.getLI())+" < "+Double.toString(obj.getVarianza())+" > "+Double.toString(obj.getLS()));

        }
     
        
    }//GEN-LAST:event_jButton2ActionPerformed

