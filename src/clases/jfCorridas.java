    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        JFileChooser filechoos = new JFileChooser();
        filechoos.setFileFilter(filefilter);
        int opcion = filechoos.showOpenDialog(this);
        
        if(opcion==JFileChooser.APPROVE_OPTION){
            ruta = filechoos.getSelectedFile().toString();
        }
      csIndependencia obj = new csIndependencia(ruta,0);
      obj.LeerExcel(ruta);
      mostrar(obj.vector(),obj.getNumfil(),obj.getNumCols());
    }//GEN-LAST:event_jButton1ActionPerformed

