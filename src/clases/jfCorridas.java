        // TODO add your handling code here:
        int por =Integer.parseInt (txt_porcien.getText());
        csIndependencia obj = new csIndependencia(ruta,por);
        //obj.ObtenerSize(ruta);
        obj.LeerExcel(ruta);
        //obj.verValores();
        obj.determinarCorridas();
        //obj.verBin();
        obj.determinarCorridas();
        obj.NumeroCorridas();
        obj.CMco();
        obj.CAco();
        obj.CZo();
        obj.alpha();
        
        txt_vesperado.setText(Double.toString(obj.getMco()));
        txt_varianza.setText(Double.toString(obj.getAco()));
        txt_estadisticoz.setText(Double.toString(obj.getZo()));
        
        boolean res = obj.verificar();
        if(res==true){
        txt_fin.setText(Double.toString(obj.getZo())+" < "+Double.toString(obj.getEquivalente()));
        }else if(res==false){
            txt_fin.setText(Double.toString(obj.getZo())+" > "+Double.toString(obj.getEquivalente()));
        
        }
        
       
          
        
